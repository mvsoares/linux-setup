ONLY_STEPS=()
SKIP_STEPS=()

while [[ $# -gt 0 ]]; do
    case "$1" in
        --reset)
            if [[ -n "${2:-}" && "$2" =~ ^[0-9]+$ ]]; then
                reset_step "$2"
                echo -e "  ${YELLOW}‚ö†${RESET}  Step $2 checkpoint cleared"
                shift
            else
                rm -rf "$STATE_DIR"
                echo -e "  ${YELLOW}‚ö†${RESET}  All checkpoints cleared ‚ÄĒ running full install"
            fi
            ;;
        --only)
            IFS=',' read -ra ONLY_STEPS <<< "${2:-}"
            shift
            ;;
        --skip)
            IFS=',' read -ra SKIP_STEPS <<< "${2:-}"
            shift
            ;;
        --list)
            echo ""
            echo -e "${BOLD}Available modules:${RESET}"
            for i in "${!MODULES[@]}"; do
                local_step=$((i + 1))
                local_desc="${MODULES[$i]#*:}"
                local_file="${MODULES[$i]%%:*}"
                echo -e "  ${CYAN}${local_step})${RESET} ${local_desc} ${DIM}(${local_file})${RESET}"
            done
            echo ""
            exit 0
            ;;
        --help|-h)
            echo "Usage: sudo bash setup.sh [OPTIONS]"
            echo ""
            echo "Options:"
            echo "  --reset [N]     Clear checkpoint(s) and re-run (all or step N)"
            echo "  --only 1,3,5    Run only specific steps"
            echo "  --skip 2        Skip specific steps (e.g., NVIDIA on non-GPU)"
            echo "  --list          List all available modules"
            echo "  --help          Show this help"
            exit 0
            ;;
    esac
    shift
done

# ‚ĒÄ‚ĒÄ Should we run this step? ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ
should_run_step() {
    local n="$1"
    if [[ ${#ONLY_STEPS[@]} -gt 0 ]]; then
        for s in "${ONLY_STEPS[@]}"; do [[ "$s" == "$n" ]] && return 0; done
        return 1
    fi
    for s in "${SKIP_STEPS[@]}"; do [[ "$s" == "$n" ]] && return 1; done
    return 0
}

